""" Return All Chars in a string in alphabetical order
    Reverse = False by default
"""


# Return all chars in the string in non-alphabetical order
print(sorted("Alphabetical", reverse=True))  # ['t', 'p', 'l', 'l', 'i', 'h', 'e', 'c', 'b', 'a', 'a', 'A']


# Return all chars in the string in non-alphabetical order
str = "Avocado"
print(sorted(str, reverse=False))  # False is default ['A', 'a', 'c', 'd', 'o', 'o', 'v']
